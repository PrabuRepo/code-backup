package com.web.hackersrank.problems;

import java.util.Scanner;

class MaxPairs {

	public static void main(String[] args) {
		int t, n;
		Scanner scanner = new Scanner(System.in);
		t = scanner.nextInt();
		while(t-- > 0) {
			n = scanner.nextInt();
			System.out.println(n+1);
		}
		scanner.close();
	}

}
